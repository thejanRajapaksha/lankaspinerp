    public function Cuttypeedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_cuttype');
        $this->db->where('idtbl_cuttype', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_cuttype;
        $obj->cuttype=$respond->row(0)->cuttype;
        echo json_encode($obj);
    }

