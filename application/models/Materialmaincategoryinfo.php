    public function Materialmaincategoryedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_material_main_cat');
        $this->db->where('idtbl_material_main_cat', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_material_main_cat;
        $obj->categoryname=$respond->row(0)->categoryname;
        echo json_encode($obj);
    }

    public function GetMainCatList() {
		$this->db->select('idtbl_material_main_cat, categoryname');
