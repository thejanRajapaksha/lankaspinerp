    public function Fliinformationedit(){
        $recordID=$this->input->post('recordID');

        $this->db->select('*');
        $this->db->from('tbl_flidata');
        $this->db->where('idtbl_flidata', $recordID);
        $this->db->where('status', 1);

        $respond=$this->db->get();

        $obj=new stdClass();
        $obj->id=$respond->row(0)->idtbl_flidata;
        $obj->name=$respond->row(0)->fli_name;
        $obj->flitype=$respond->row(0)->fli_type;
        echo json_encode($obj);
    }

